namespace Himesyo.Check
{
    /// <summary>
    /// CRC 算法模式。
    /// </summary>
    public enum CrcMode
    {
        /// <summary>
        /// 表示 8 位 CRC 算法。
        /// </summary>
        Crc8 = 8,
        /// <summary>
        /// 表示 16 位 CRC 算法。
        /// </summary>
        Crc16 = 16,
        /// <summary>
        /// 表示 32 位 CRC 算法。
        /// </summary>
        Crc32 = 32,
        /// <summary>
        /// 表示 64 位 CRC 算法。
        /// </summary>
        Crc64 = 64
    }
}
